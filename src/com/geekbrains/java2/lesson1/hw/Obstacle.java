package com.geekbrains.java2.lesson1.hw;

public class Obstacle {
    private int HEIGHT;
    private int LENGTH;

    public Obstacle(int HEIGHT, int LENGTH) {
        this.HEIGHT = HEIGHT;
        this.LENGTH = LENGTH;
    }

    public int getHEIGHT() {
        return HEIGHT;
    }

    public int getLENGTH() {
        return LENGTH;
    }
}
